def draw_rows(rows, x, y):
    for ry, row in enumerate(rows):
        sy = y + ry
        if sy < 0 or sy >= HEIGHT:
            continue
        for rx, ch in enumerate(row):
            if ch == "1":
                sx = x + rx
                if 0 <= sx < WIDTH:
                    oled.pixel(sx, sy, 1)

